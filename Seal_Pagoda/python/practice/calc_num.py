# P297 - 练习题2
# 输入两个数字a和b，计算出a的b次方，再除以b并取整后的值。
# ================================================================================


def calc_num(a: float = .0, b: float = .0) -> None:
    result = (a ** b)//b
    print('a = {}, b = {}, a**b//b = {}'.format(a, b, result))

    return


a = float(input('请输入a的值：'))
b = float(input('请输入b的值：'))
calc_num(a, b)

# 输入一个半径，计算出园的周长和面积
# ================================================================================


def calc_circle(radius=.0) -> None:
    pi = 3.14
    perimeter = (2 * pi * radius)
    area = pi * radius ** 2
    print('半径为{}的圆，周长是{}，面积是{}。'.format(radius, perimeter, area))

    return


calc_circle(5)
